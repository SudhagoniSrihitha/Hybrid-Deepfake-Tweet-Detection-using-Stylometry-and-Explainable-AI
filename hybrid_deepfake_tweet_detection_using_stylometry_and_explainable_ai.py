def generate_natural_explanation(text, prediction, shap_values, stylometry_feats):
    label = "MACHINE" if prediction == 1 else "HUMAN"
    reasons = []
    if stylometry_feats[2] < 0.4:
        reasons.append("low lexical diversity (type-token ratio)")
    if stylometry_feats[0] < 3.5:
        reasons.append("short average word length")
    if stylometry_feats[4] < 0.05:
        reasons.append("few uppercase letters (robotic tone)")
    if shap_values.values.sum() > 0.5:
        reasons.append("semantic features strongly suggest AI pattern")

    return f"📥 Input Tweet: \"{text}\"\n\n🧠 Predicted Class: **{label}**\n🧠 Reason: " + ", ".join(reasons)

"""# **Heatmap Function**"""

def plot_shap_heatmap(shap_values_array, top_n=10):
    top_indices = np.argsort(np.abs(shap_values_array))[-top_n:][::-1]
    top_shap_values = shap_values_array[top_indices]
    feature_names = [f"Feature {i}" for i in top_indices]

    plt.figure(figsize=(10, 1))
    sns.heatmap(
        [top_shap_values],
        annot=True,
        fmt=".3f",
        cmap="coolwarm",
        xticklabels=feature_names,
        yticklabels=["SHAP"],
        cbar=True
    )
    plt.title("Top SHAP Feature Contributions")
    plt.tight_layout()
    plt.show()

