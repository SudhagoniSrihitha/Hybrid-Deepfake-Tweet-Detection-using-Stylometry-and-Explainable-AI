from nltk import word_tokenize, sent_tokenize

def extract_stylometric_features(text):
    words = word_tokenize(text)
    sentences = sent_tokenize(text)
    avg_word_len = np.mean([len(w) for w in words]) if words else 0
    avg_sent_len = np.mean([len(s.split()) for s in sentences]) if sentences else 0
    type_token_ratio = len(set(words)) / len(words) if words else 0
    punctuation_count = sum([1 for c in text if c in ".,;!?"])
    upper_case_ratio = sum(1 for c in text if c.isupper()) / len(text) if text else 0

    return [avg_word_len, avg_sent_len, type_token_ratio, punctuation_count, upper_case_ratio]

stylometric_features = df['text'].apply(extract_stylometric_features)
